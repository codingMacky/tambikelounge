//page
var selectedItemValue = localStorage.getItem("activeTab");
document.getElementById("activeTab").innerHTML = selectedItemValue;

let selectedTab = selectedItemValue;
var filterByActiveTab =
  selectedTab == "Parts & Accessories" ? "parts" : "gears";

document.getElementById("listOfProductContainer").innerHTML = tambikeProducts
  .filter((tambikeProduct) => tambikeProduct.type == filterByActiveTab)
  .map((filteredProduct) => {
    return `<div class="product-container">
      <div class="product-image-container">
        <img src=${filteredProduct.imageUrl} class="product-image" />
      </div>
      <div class="details-container">
        <span class="product-name">${filteredProduct.productName}</span>
        <span class="product-price">${filteredProduct.price}</span>
      </div>
    </div>`;
  })
  .join("");

//redirection
function handleLogoClick() {
  location.href = "/";
}

function handleTabClick(activeTab) {
  location.href = "/product.html";
  if (typeof Storage !== "undefined") {
    localStorage.setItem("activeTab", activeTab);
  }
}

//Header
function handleMobileMenuClick(type) {
  var menuElement = document.querySelector(`${type}`);
  menuElement.classList.toggle("show");
}
