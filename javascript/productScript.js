let selectedCategory = "";
let selectedBrand = "";

//function
function handleCategoryRadioClick(category) {
  selectedCategory = category;
  displayFilteredProduct(selectedCategory, selectedBrand);
}

function handleBrandRadioClick(brand) {
  selectedBrand = brand;
  displayFilteredProduct(selectedCategory, selectedBrand);
}

function displayFilteredProduct(activeCategory, activeBrand) {
  const categoryText =
    activeCategory == "gear&apparels" ? "Gear & Apparels" : activeCategory;
  const brandText = categoryText == "" ? activeBrand : categoryText;
  document.querySelector("#currentCategoryId").innerText = brandText;

  if (activeCategory != "" && activeBrand == "") {
    console.log("Category");
    document.getElementById("listOfProductContainer").innerHTML =
      tambikeProducts
        .filter((filterByCategory) => filterByCategory.type == activeCategory)
        .map((filteredProduct) => {
          return `<div class="product-item-container">
      <div class="product-image-container">
        <img src=${filteredProduct.imageUrl} class="product-image" />
      </div>
      <div class="product-details-container">
        <span class="product-name">${filteredProduct.productName}</span>
        <span class="product-price">${filteredProduct.price}</span>
      </div>
    </div>`;
        })
        .join("");
  }
  if (activeCategory == "" && activeBrand != "") {
    console.log("Brand");
    document.getElementById("listOfProductContainer").innerHTML =
      tambikeProducts
        .filter((filterByCategory) => filterByCategory.brand == activeBrand)
        .map((filteredProduct) => {
          return `<div class="product-item-container">
      <div class="product-image-container">
        <img src=${filteredProduct.imageUrl} class="product-image" />
      </div>
      <div class="product-details-container">
        <span class="product-name">${filteredProduct.productName}</span>
        <span class="product-price">${filteredProduct.price}</span>
      </div>
    </div>`;
        })
        .join("");
  }
  if (activeCategory != "" && activeBrand != "") {
    console.log("Mixed");
    document.getElementById("listOfProductContainer").innerHTML =
      tambikeProducts
        .filter(
          (filterByCategory) =>
            filterByCategory.brand == activeBrand &&
            filterByCategory.type == activeCategory
        )
        .map((filteredProduct) => {
          return `<div class="product-item-container">
      <div class="product-image-container">
        <img src=${filteredProduct.imageUrl} class="product-image" />
      </div>
      <div class="product-details-container">
        <span class="product-name">${filteredProduct.productName}</span>
        <span class="product-price">${filteredProduct.price}</span>
      </div>
    </div>`;
        })
        .join("");
  }
}

